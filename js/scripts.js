$(document).ready(function() {
    $("#oilimg").click(function() {
        $('#oilimg').slideDown('1500').hide('1000');
        $("#oil").show('1500');
    });
    $("#oil").click(function() {
        $("#oil").slideUp('1500').hide('1000');
        $("#oilimg").show('1500');
    });
});

